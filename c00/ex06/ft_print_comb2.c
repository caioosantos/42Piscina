#include <unistd.h>

void	ft_print_comb2(void)
{
	char	n[5];
	int		i;
	int		j;

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			n[0] = i / 10 + '0';
			n[1] = i % 10 + '0';
			n[2] = ' ';
			n[3] = j / 10 + '0';
			n[4] = j % 10 + '0';
			write(1, n, 5);
			if (i != 98 || j != 99)
				write(1, ", ", 2);
			j++;
		}
		i++;
	}
}

int	main(void)
{
	ft_print_comb2();
	return (0);
}
